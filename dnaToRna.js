const dnaToRna = (dna) => {
    let rnk = "";
    for(let i = 0; i < dna.length; i++){
        if(dna[i]==="G"){
            rnk = rnk + "C";
        }else if(dna[i]==="C"){
            rnk = rnk + "G";
        }else if(dna[i]==="T"){
            rnk = rnk + "A";
        }else if(dna[i]==="A"){
            rnk = rnk + "U";
        }else if(dna === ""){
            rnk = "";
        }else{
            rnk = null;
            break;
        };
    };
    return rnk;
};
//========================================

