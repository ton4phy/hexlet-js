const areBracketsBalanced = (str) => {
    let counter = 0;

    if(str.length % 2 > 0 || str[0] === ")"){
        return false;
    }else if(str === ""){
        return true;
    };

    for(let i = 0; i < str.length; i++){
        if(str[i] === "("){
            counter = counter + 1;
        }else if(str[i] === ")"){
            counter = counter - 1;
        };
    };

    return counter === 0;
};
//==================================================

